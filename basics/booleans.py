"""An explanation of Boolean values and statements"""

# A Boolean is a True/False statement
print(True)
# Booleans can be used to determine if two values are equal
# The double equal sign is a test for if two values are equivalent
print(3 == 3)
print('hello' == 'HELLO')
print(3.0 == 3)

# Booleans can be used to determine if something is in a string/list/dictionary:
print('a' in 'antelope')
print('x' in 'mongoose')
