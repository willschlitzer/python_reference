"""An explanation of variables"""

# Data can be stored in a variable
# Variables can store all types of data, including integers,
# floats, strings, classes, lists, and dictionaries

# Variable names must begin with letters
# Variables can include numbers and underscores
my_string = 'Hello'
print(my_string)
my_int = 64
print(my_int)
my_float = 2.5
print(my_float)

my_list1 = [1,2,3]
my_list2 = [4,5,6]
print(my_list1, my_list2)

my_dict = {'first':1, 'second':2}
print(my_dict)